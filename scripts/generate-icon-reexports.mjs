#!/usr/bin/env node
/**
 * Regenerates template-antd-shared/src/components/icons/icon-reexports.generated.ts — a
 * committed, plain-ESM `export { Name } from '@ant-design/icons'` line per real icon
 * component. Exists because `import * as Icons from '@ant-design/icons'` (a namespace
 * import of the raw npm CJS package) crashes Next's Turbopack SSR bundler with
 * "'ownKeys' on proxy: trap returned extra keys but proxy target is non-extensible" — a
 * Turbopack/CJS-interop bug triggered specifically by namespace-importing that package.
 * Re-exporting each icon individually through our own plain-ESM file sidesteps it: Turbopack
 * only needs to resolve named bindings one at a time, never wrap the whole CJS module in a
 * namespace proxy. Also emits `iconReexportNames` (a plain string[]) so consumers never need
 * to call Object.keys() on the namespace import either.
 *
 * Run manually after bumping the `antd` devDependency (which brings @ant-design/icons with
 * it). Usage: node scripts/generate-icon-reexports.mjs
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import * as Icons from '@ant-design/icons'

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..')
const outPath = path.join(root, 'template-antd-shared/src/components/icons/icon-reexports.generated.ts')

const ICON_NAME_RE = /^[A-Z][A-Za-z0-9]*(Outlined|Filled|TwoTone)$/
const names = Object.keys(Icons).filter((n) => ICON_NAME_RE.test(n)).sort()

const content = `/**
 * Generated by scripts/generate-icon-reexports.mjs — do not hand-edit. Regenerate with
 * \`node scripts/generate-icon-reexports.mjs\` after bumping the \`antd\` devDependency.
 * See that script for why this file exists instead of a plain namespace import.
 */
export {
${names.map((n) => `  ${n},`).join('\n')}
} from '@ant-design/icons'

export const iconReexportNames: string[] = ${JSON.stringify(names)}
`

fs.writeFileSync(outPath, content)
console.log(`Wrote ${names.length} icon re-exports to ${path.relative(root, outPath)}`)
