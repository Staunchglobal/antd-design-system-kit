#!/usr/bin/env node
/**
 * Derives src/generated/registry.ts from template-antd-next/src/app/design-system/_lib/nav.ts
 * (the canonical component/group list — NOT copied into consumer projects). Far simpler than
 * the shadcn kit's build-registry.mjs: there's no vendored component source to scan for
 * uiDeps/npmDeps/cssFile, since every Ant Design component ships from the single `antd`
 * package regardless of selection. This script's only real job is cross-validating that
 * every nav slug has a matching `_sections/<slug>.tsx` demo file in BOTH template-antd-next
 * and template-antd-vite (byte-identical demo pairs), so a drifted rename fails the build
 * loudly instead of silently shipping a broken import.
 *
 * Run with `npm run build:registry` whenever nav.ts or the _sections/ directories change.
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..')
const navPath = path.join(root, 'template-antd-next/src/app/design-system/_lib/nav.ts')
const nextSectionsDir = path.join(root, 'template-antd-next/src/app/design-system/_sections')
const viteSectionsDir = path.join(root, 'template-antd-vite/src/design-system/_sections')

function parseNavGroups(src) {
  const groups = []
  const groupRe = /\{\s*title:\s*'([^']+)',\s*items:\s*\[([\s\S]*?)\]\s*,?\s*\}/g
  let gm
  while ((gm = groupRe.exec(src))) {
    const [, title, itemsBlock] = gm
    const items = []
    const itemRe = /\{\s*id:\s*'([^']+)',\s*label:\s*'([^']+)'\s*\}/g
    let im
    while ((im = itemRe.exec(itemsBlock))) items.push({ id: im[1], label: im[2] })
    groups.push({ title, items })
  }
  return groups
}

const navSrc = fs.readFileSync(navPath, 'utf8')
const navGroups = parseNavGroups(navSrc)
const allSlugs = new Set(navGroups.flatMap((g) => g.items.map((i) => i.id)))

for (const [label, dir] of [
  ['template-antd-next', nextSectionsDir],
  ['template-antd-vite', viteSectionsDir],
]) {
  const missing = [...allSlugs].filter((slug) => !fs.existsSync(path.join(dir, `${slug}.tsx`)))
  if (missing.length) {
    throw new Error(`nav.ts references slugs with no matching ${label} _sections/*.tsx file: ${missing.join(', ')}`)
  }
  const sectionFiles = fs.readdirSync(dir).filter((f) => f.endsWith('.tsx'))
  const orphaned = sectionFiles.filter((f) => !allSlugs.has(f.replace(/\.tsx$/, '')))
  if (orphaned.length) {
    throw new Error(`${label} _sections/*.tsx files with no matching nav.ts entry: ${orphaned.join(', ')}`)
  }
}

// tokenGroupIds is populated starting Phase 4 (per-component token editing) — empty for now.
const components = {}
for (const slug of allSlugs) {
  components[slug] = { demoFile: `${slug}.tsx`, tokenGroupIds: [] }
}

const groups = navGroups.map((g) => ({
  title: g.title,
  items: g.items.map((i) => ({ slug: i.id, label: i.label, demoFile: `${i.id}.tsx` })),
}))

const out = `// Generated by scripts/build-registry.mjs — do not hand-edit. Re-run \`npm run build:registry\`.
export type ComponentEntry = {
  demoFile: string
  tokenGroupIds: string[]
}

export type NavItem = { slug: string; label: string; demoFile: string }
export type NavGroup = { title: string; items: NavItem[] }

export const COMPONENTS: Record<string, ComponentEntry> = ${JSON.stringify(components, null, 2)}

export const GROUPS: NavGroup[] = ${JSON.stringify(groups, null, 2)}
`

const outPath = path.join(root, 'src/generated/registry.ts')
fs.mkdirSync(path.dirname(outPath), { recursive: true })
fs.writeFileSync(outPath, out)
console.log(`Wrote ${path.relative(root, outPath)} (${Object.keys(components).length} components, ${groups.length} groups)`)
